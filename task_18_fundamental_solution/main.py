"""
===========================================================================
Завдання 18. Побудова графіків фундаментального розв'язку для
розповсюдження одиничного теплового імпульсу в задачі Коші
в залежності від часу t.
===========================================================================

Теоретична основа (за книгою С. Фарлоу, "Рівняння в частинних похідних"):
----------------------------------------------------------------------
Розглядаємо рівняння теплопровідності на нескінченній прямій:
    u_t = α² · u_xx,   -∞ < x < ∞,  t > 0

з початковою умовою — одиничний тепловий імпульс (дельта-функція Дірака):
    u(x, 0) = δ(x)

Фундаментальний розв'язок (функція Гріна / ядро теплопровідності):
    G(x, t) = 1 / √(4πα²t) · exp(-x² / (4α²t))

Ця формула описує, як одиничний тепловий імпульс, зосереджений
у початковий момент у точці x=0, розповсюджується з плином часу.

Властивості фундаментального розв'язку:
- ∫G(x,t)dx = 1 для будь-якого t > 0 (збереження кількості тепла)
- При t → 0+ розв'язок прямує до δ(x) (дельта-функції)
- При t → ∞ розв'язок прямує до нуля рівномірно (тепло розповсюджується)
- Максимум G(x,t) = 1/√(4πα²t) спадає як 1/√t
===========================================================================
"""

import numpy as np
import matplotlib.pyplot as plt
from matplotlib.gridspec import GridSpec

# ============================================================
# Параметри задачі
# ============================================================
alpha = 1.0          # Коефіцієнт температуропровідності (α)
x_min, x_max = -5, 5 # Межі просторової області
N_x = 500            # Кількість точок по x

# Моменти часу для побудови графіків
# (обрано декілька значень, щоб побачити еволюцію розв'язку)
time_values = [0.01, 0.05, 0.1, 0.5, 1.0, 2.0]

# ============================================================
# Визначення фундаментального розв'язку
# ============================================================
def fundamental_solution(x, t, alpha):
    """
    Фундаментальний розв'язок (ядро теплопровідності / функція Гріна).
    
    Параметри:
    ----------
    x : array-like
        Просторова координата
    t : float
        Момент часу (t > 0)
    alpha : float
        Коефіцієнт температуропровідності
    
    Повертає:
    ---------
    G(x, t) = 1 / √(4πα²t) · exp(-x² / (4α²t))
    
    Це розв'язок задачі Коші для рівняння теплопровідності
    з початковою умовою u(x,0) = δ(x) (дельта-функція Дірака).
    """
    # Знаменник під коренем: 4πα²t
    denominator = 4 * np.pi * alpha**2 * t
    
    # Нормувальний множник: 1/√(4πα²t)
    prefactor = 1.0 / np.sqrt(denominator)
    
    # Експоненціальна частина: exp(-x² / (4α²t))
    exponent = -x**2 / (4 * alpha**2 * t)
    
    return prefactor * np.exp(exponent)


# ============================================================
# Масив просторових координат
# ============================================================
x = np.linspace(x_min, x_max, N_x)

# ============================================================
# Побудова графіків
# ============================================================

# --- Графік 1: Профілі G(x,t) для різних моментів часу ---
fig = plt.figure(figsize=(16, 10))
gs = GridSpec(2, 2, figure=fig, hspace=0.35, wspace=0.3)

# Основний графік — всі профілі на одному полотні
ax1 = fig.add_subplot(gs[0, :])

# Кольорова палітра для наочності
colors = plt.cm.plasma(np.linspace(0.1, 0.9, len(time_values)))

for i, t in enumerate(time_values):
    # Обчислюємо фундаментальний розв'язок для даного t
    G = fundamental_solution(x, t, alpha)
    
    # Малюємо профіль
    ax1.plot(x, G, color=colors[i], linewidth=2,
             label=f't = {t:.2f}  (max = {1/np.sqrt(4*np.pi*alpha**2*t):.3f})')

ax1.set_xlabel('x (просторова координата)', fontsize=12)
ax1.set_ylabel('G(x, t)', fontsize=12)
ax1.set_title(
    f'Фундаментальний розв\'язок рівняння теплопровідності\n'
    f'G(x,t) = 1/√(4πα²t) · exp(-x²/(4α²t)),  α = {alpha}',
    fontsize=14, fontweight='bold'
)
ax1.legend(fontsize=10, loc='upper right')
ax1.grid(True, alpha=0.3)
ax1.set_xlim(x_min, x_max)

# --- Графік 2: Максимум G(x,t) як функція часу ---
ax2 = fig.add_subplot(gs[1, 0])

t_array = np.linspace(0.01, 3.0, 200)
# Максимум фундаментального розв'язку (при x=0):
# G(0, t) = 1 / √(4πα²t)
G_max = 1.0 / np.sqrt(4 * np.pi * alpha**2 * t_array)

ax2.plot(t_array, G_max, 'r-', linewidth=2)
ax2.set_xlabel('t (час)', fontsize=12)
ax2.set_ylabel('G(0, t) = max G(x,t)', fontsize=12)
ax2.set_title('Залежність максимуму G від часу\n(спадає як 1/√t)', fontsize=12, fontweight='bold')
ax2.grid(True, alpha=0.3)

# --- Графік 3: "Ширина" розподілу як функція часу ---
ax3 = fig.add_subplot(gs[1, 1])

# Стандартне відхилення Гауссівського розподілу: σ = √(2α²t)
sigma_array = np.sqrt(2 * alpha**2 * t_array)

ax3.plot(t_array, sigma_array, 'b-', linewidth=2, label='σ = √(2α²t)')
ax3.plot(t_array, 2 * sigma_array, 'g--', linewidth=1.5, label='2σ (95% тепла)')
ax3.set_xlabel('t (час)', fontsize=12)
ax3.set_ylabel('σ (стандартне відхилення)', fontsize=12)
ax3.set_title('Ширина теплового імпульсу\n(зростає як √t)', fontsize=12, fontweight='bold')
ax3.legend(fontsize=10)
ax3.grid(True, alpha=0.3)

plt.suptitle('Завдання 18: Розповсюдження одиничного теплового імпульсу', 
             fontsize=16, fontweight='bold', y=1.02)
plt.tight_layout()
plt.savefig('graphs.png', dpi=150, bbox_inches='tight')
plt.show()

# ============================================================
# Додатковий графік: 3D-візуалізація G(x,t)
# ============================================================
fig2, ax4 = plt.subplots(figsize=(12, 8), subplot_kw={'projection': '3d'})

# Створюємо сітку (x, t)
x_3d = np.linspace(-4, 4, 200)
t_3d = np.linspace(0.02, 2.0, 100)
X, T = np.meshgrid(x_3d, t_3d)

# Обчислюємо G(x,t) на всій сітці
G_3d = fundamental_solution(X, T, alpha)

# Малюємо поверхню
surf = ax4.plot_surface(X, T, G_3d, cmap='inferno', alpha=0.85,
                        edgecolor='none', antialiased=True)

ax4.set_xlabel('x', fontsize=12)
ax4.set_ylabel('t', fontsize=12)
ax4.set_zlabel('G(x, t)', fontsize=12)
ax4.set_title(
    'Завдання 18: 3D-візуалізація фундаментального розв\'язку\n'
    'G(x,t) = 1/√(4πα²t) · exp(-x²/(4α²t))',
    fontsize=13, fontweight='bold'
)

fig2.colorbar(surf, ax=ax4, shrink=0.5, aspect=10, label='G(x, t)')
ax4.view_init(elev=25, azim=-60)
plt.tight_layout()
plt.savefig('graphs_3d.png', dpi=150, bbox_inches='tight')
plt.show()

print("=" * 60)
print("Завдання 18 виконано!")
